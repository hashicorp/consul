const { test: base, expect } = require('@playwright/test');

/**
 * PeeringApiHelper — creates and manages peerings via the Consul HTTP API.
 *
 * The peering lifecycle spans two Consul instances:
 *  - The "acceptor" generates a token via POST /v1/peering/token
 *  - The "dialer" establishes the connection via POST /v1/peering/establish
 *
 * This helper is instantiated once per instance (acceptor/dialer) and tracks
 * peer names for cleanup after each test.
 */
class PeeringApiHelper {
  constructor(request, baseURL) {
    this.request = request;
    this.baseURL = baseURL;
    this.token = process.env.CONSUL_UI_TEST_TOKEN;
    this._trackedNames = [];
  }

  get headers() {
    const h = {};
    if (this.token) h['X-Consul-Token'] = this.token;
    return h;
  }

  /**
   * Generates a peering token on this instance (acceptor side).
   * @param {string} peerName - The name for the peer on this side.
   * @returns {Promise<string>} The raw PeeringToken string.
   */
  async generateToken(peerName) {
    const response = await this.request.post(`${this.baseURL}/v1/peering/token`, {
      headers: this.headers,
      data: { PeerName: peerName },
    });
    expect(response.ok(), `Generate token failed: ${response.status()}`).toBeTruthy();
    const body = await response.json();
    this._trackedNames.push(peerName);
    return body.PeeringToken;
  }

  /**
   * Establishes a peering on this instance (dialer side) using a token from
   * the acceptor.
   * @param {string} peerName - The name for the peer on this side.
   * @param {string} peeringToken - Token generated by the acceptor.
   */
  async establish(peerName, peeringToken) {
    const response = await this.request.post(`${this.baseURL}/v1/peering/establish`, {
      headers: this.headers,
      data: { PeerName: peerName, PeeringToken: peeringToken },
    });
    expect(response.ok(), `Establish peering failed: ${response.status()}`).toBeTruthy();
    this._trackedNames.push(peerName);
  }

  /**
   * Reads a peer by name from this instance.
   * @param {string} peerName
   * @returns {Promise<object>}
   */
  async read(peerName) {
    const response = await this.request.get(`${this.baseURL}/v1/peering/${peerName}`, {
      headers: this.headers,
    });
    expect(response.ok(), `Peer read failed: ${response.status()}`).toBeTruthy();
    return response.json();
  }

  /**
   * Tracks a peer name for cleanup without creating it here (useful when the
   * peer was created through the UI rather than the API).
   * @param {string} peerName
   */
  track(peerName) {
    if (!this._trackedNames.includes(peerName)) {
      this._trackedNames.push(peerName);
    }
  }

  /**
   * Deletes a peer by name from this instance.
   * @param {string} peerName
   */
  async delete(peerName) {
    const response = await this.request.delete(`${this.baseURL}/v1/peering/${peerName}`, {
      headers: this.headers,
    });
    // 404 is acceptable — peer may already be gone.
    if (!response.ok() && response.status() !== 404) {
      console.warn(`Warning: failed to delete peer ${peerName} (${response.status()})`);
    }
    this._trackedNames = this._trackedNames.filter((n) => n !== peerName);
  }

  /**
   * Deletes all peers tracked by this helper instance.
   */
  async cleanup() {
    for (const name of [...this._trackedNames]) {
      await this.delete(name);
    }
  }

  /**
   * Adds the given peer name as a consumer to every service in the
   * exported-services singleton config entry on this instance.
   *
   * The original config is saved so restoreExportedServices() can put it back.
   * Safe to call when no exported-services entry exists yet (creates a minimal
   * wildcard entry in that case).
   *
   * @param {string} peerName - the acceptor-side peer name to add as a consumer.
   */
  async addPeerToExportedServices(peerName) {
    // Read current exported-services config entry (may not exist).
    const readRes = await this.request.get(`${this.baseURL}/v1/config/exported-services/default`, {
      headers: this.headers,
    });

    if (readRes.ok()) {
      this._savedExportedServices = await readRes.json();
    } else {
      this._savedExportedServices = null; // no pre-existing entry
    }

    let services;
    if (this._savedExportedServices?.Services?.length > 0) {
      // Add our test peer as an additional consumer on each existing service.
      // Do NOT forward the Namespace field — CE rejects empty-string namespaces
      // (which is what the consul-ui-testing environment returns in API responses).
      services = this._savedExportedServices.Services.map((svc) => ({
        Name: svc.Name,
        Consumers: [...(svc.Consumers || []), { Peer: peerName }],
      }));
    } else {
      // No existing exported-services entry — discover real services from the
      // catalog and export them. The Consul API rejects wildcard (*) service
      // names in exported-services config entries, so we must enumerate.
      const catalogRes = await this.request.get(`${this.baseURL}/v1/catalog/services`, {
        headers: this.headers,
      });
      const serviceMap = catalogRes.ok() ? await catalogRes.json() : {};
      // Exclude the built-in 'consul' service which cannot be exported.
      const serviceNames = Object.keys(serviceMap).filter((n) => n !== 'consul');

      if (serviceNames.length === 0) {
        throw new Error(
          'No exportable services found in catalog; cannot set up exported-services config'
        );
      }

      services = serviceNames.map((name) => ({
        Name: name,
        Consumers: [{ Peer: peerName }],
      }));
    }

    const config = {
      Kind: 'exported-services',
      Name: this._savedExportedServices?.Name || 'default',
      Services: services,
    };

    const writeRes = await this.request.put(`${this.baseURL}/v1/config`, {
      headers: this.headers,
      data: config,
    });
    if (!writeRes.ok()) {
      const errBody = await writeRes.text().catch(() => '(unreadable)');
      throw new Error(
        `Failed to update exported-services config (${writeRes.status()}): ${errBody}`
      );
    }
  }

  /**
   * Restores the exported-services config entry to the state saved by
   * addPeerToExportedServices(). No-op if that method was never called.
   */
  async restoreExportedServices() {
    // undefined = addPeerToExportedServices was never called; skip.
    if (this._savedExportedServices === undefined) return;

    if (this._savedExportedServices) {
      const res = await this.request.put(`${this.baseURL}/v1/config`, {
        headers: this.headers,
        data: this._savedExportedServices,
      });
      if (!res.ok()) {
        console.warn(`Warning: failed to restore exported-services config (${res.status()})`);
      }
    }
    this._savedExportedServices = undefined;
  }
}

/**
 * PeeringsPage — page-object helper for the Peerings list and detail pages.
 * Constructed with the page instance and the baseURL of that specific UI.
 */
class PeeringsPage {
  constructor(page, baseURL, dc = null) {
    this.page = page;
    this.baseURL = baseURL;
    this._dc = dc;
  }

  // ── Navigation ─────────────────────────────────────────────────────────────

  async getDc() {
    if (this._dc) return this._dc;
    try {
      const res = await this.page.request.get(`${this.baseURL}/v1/agent/self`);
      const body = await res.json();
      this._dc = body?.Config?.Datacenter || 'dc1';
    } catch (_) {
      this._dc = 'dc1';
    }
    return this._dc;
  }

  async goto() {
    const dc = await this.getDc();
    await this.page.goto(`${this.baseURL}/ui/${dc}/peers`, {
      waitUntil: 'domcontentloaded',
    });
  }

  // ── List ───────────────────────────────────────────────────────────────────

  get heading() {
    return this.page.getByRole('heading', { name: 'Peers' });
  }

  /**
   * Returns the peer name link/element for a named peer row.
   * Works in both dev builds (data-test-peer present) and production builds
   * (data-test-peer stripped — the Consul binary UI on port 8501 is a production build).
   * @param {string} peerName
   */
  peerRow(peerName) {
    return this.page
      .locator(`[data-test-peer="${peerName}"], a[href*="/peers/${peerName}"]`)
      .first();
  }

  /**
   * Returns the full list-row <li> element for a named peer.
   * Works in both dev builds (data-test-list-row + data-test-peer) and production
   * builds where those attributes are stripped (matched via href instead).
   * @param {string} peerName
   */
  peerListRow(peerName) {
    return this.page
      .locator(
        `[data-test-list-row]:has([data-test-peer="${peerName}"]), li:has(a[href*="/peers/${peerName}"])`
      )
      .first();
  }

  /**
   * Waits for a peer row to appear in the list.
   * @param {string} peerName
   */
  async waitForPeerInList(peerName, timeout = 20000) {
    await expect(this.peerRow(peerName)).toBeVisible({ timeout });
  }

  // ── "Add peer connection" modal ────────────────────────────────────────────

  get addPeerButton() {
    return this.page.getByRole('button', { name: 'Add peer connection' });
  }

  get generateTokenTab() {
    // Scope to the "Secondary" tab navigation inside the modal so we don't
    // accidentally match the form submit button which also reads "Generate token".
    return this.page
      .getByRole('navigation', { name: 'Secondary' })
      .getByRole('button', { name: 'Generate token', exact: true });
  }

  get establishPeeringTab() {
    return this.page
      .getByRole('navigation', { name: 'Secondary' })
      .getByRole('button', { name: 'Establish peering', exact: true });
  }

  get peerNameInput() {
    return this.page.getByRole('textbox', { name: 'Name' });
  }

  get tokenTextarea() {
    return this.page.getByRole('textbox', { name: 'Token' });
  }

  get generateTokenButton() {
    // Two elements have the text "Generate token": the tab button (type=button)
    // and the form submit button (type=submit). Target the submit explicitly.
    return this.page.locator('button[type="submit"]').filter({ hasText: 'Generate token' });
  }

  get addPeerSubmitButton() {
    // Use exact:true so "Add peer connection" (the list-page trigger button) is not matched.
    return this.page.getByRole('button', { name: 'Add peer', exact: true });
  }

  get copyTokenButton() {
    // "Copy Token to the clipboard" (aria-label icon) also matches without exact:true.
    return this.page.getByRole('button', { name: 'Copy token', exact: true });
  }

  get closeModalButton() {
    return this.page.getByRole('button', { name: 'Close', exact: true });
  }

  /**
   * Opens the "Add peer connection" modal.
   */
  async openAddPeerModal() {
    await this.addPeerButton.click();
    await expect(this.generateTokenTab).toBeVisible({ timeout: 10000 });
  }

  /**
   * Generates a peering token through the UI.
   * Returns the token string extracted from the CopyableCode element.
   * @param {string} peerName
   * @returns {Promise<string>} The generated peering token.
   */
  async generateTokenViaUI(peerName) {
    await this.openAddPeerModal();
    await this.peerNameInput.fill(peerName);
    await this.generateTokenButton.click();

    // Wait for the token display screen.
    await expect(this.copyTokenButton).toBeVisible({ timeout: 15000 });

    // Extract the token value from the CopyableCode component.
    const token = await this.page
      .locator('[data-test-copyable-value]')
      .or(this.page.locator('pre[data-test]'))
      .or(this.page.locator('.copyable-code code'))
      .first()
      .textContent({ timeout: 10000 });

    await this.closeModalButton.click();
    return token.trim();
  }

  /**
   * Establishes a peering through the UI by pasting a token on the
   * "Establish peering" tab.
   * @param {string} peerName
   * @param {string} peeringToken
   */
  async establishPeeringViaUI(peerName, peeringToken) {
    await this.openAddPeerModal();
    await this.establishPeeringTab.click();
    await this.peerNameInput.fill(peerName);
    await this.tokenTextarea.fill(peeringToken);
    const submitButton = this.addPeerSubmitButton;
    await expect(submitButton).toBeEnabled({ timeout: 10000 });
    await submitButton.click();
  }
}

/**
 * Extended test object providing peering-specific fixtures.
 *
 * Fixtures:
 *  - peerBaseURL    — URL of the second Consul instance (port 8501)
 *  - peerPage       — a Playwright Page scoped to peerBaseURL
 *  - acceptorApi    — PeeringApiHelper for the primary (acceptor) instance
 *  - dialerApi      — PeeringApiHelper for the peer (dialer) instance
 *  - peeringsPage   — PeeringsPage page-object for the primary instance
 *  - peerPeeringsPage — PeeringsPage page-object for the peer instance
 */
const test = base.extend({
  // Option fixture so playwright.config.js can override it via `use.peerBaseURL`.
  // eslint-disable-next-line ember/avoid-leaking-state-in-ember-objects
  peerBaseURL: [process.env.PLAYWRIGHT_PEER_URL || 'http://localhost:8501', { option: true }],

  /**
   * A separate browser page navigating against the second Consul instance.
   * Uses its own browser context so cookies/storage are independent.
   */
  peerPage: async ({ browser, peerBaseURL }, use) => {
    const context = await browser.newContext({ baseURL: peerBaseURL });
    const page = await context.newPage();
    await use(page);
    await context.close();
  },

  /**
   * API helper for the primary (acceptor) Consul instance.
   * Cleans up tracked peers after each test.
   */
  acceptorApi: async ({ request, baseURL }, use) => {
    const helper = new PeeringApiHelper(request, baseURL);
    await use(helper);
    await helper.restoreExportedServices();
    await helper.cleanup();
  },

  /**
   * API helper for the second (dialer) Consul instance.
   * Uses a new APIRequestContext pointing at peerBaseURL.
   * Cleans up tracked peers after each test.
   */
  dialerApi: async ({ playwright, peerBaseURL }, use) => {
    const requestContext = await playwright.request.newContext({
      baseURL: peerBaseURL,
      extraHTTPHeaders: process.env.CONSUL_UI_TEST_TOKEN
        ? { 'X-Consul-Token': process.env.CONSUL_UI_TEST_TOKEN }
        : {},
    });
    const helper = new PeeringApiHelper(requestContext, peerBaseURL);
    await use(helper);
    await helper.cleanup();
    await requestContext.dispose();
  },

  /**
   * PeeringsPage page-object for the primary Consul instance.
   */
  peeringsPage: async ({ page, baseURL }, use) => {
    await use(new PeeringsPage(page, baseURL, 'dc1'));
  },

  /**
   * PeeringsPage page-object for the second Consul instance.
   * The dc is detected dynamically from the agent's self endpoint.
   */
  peerPeeringsPage: async ({ peerPage, peerBaseURL }, use) => {
    await use(new PeeringsPage(peerPage, peerBaseURL));
  },
});

module.exports = { test, expect, PeeringApiHelper, PeeringsPage };
